    public void readTable(List<model.Consulta> dao){
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        model.setNumRows(0);
        
        for(model.Consulta c: dao){
            if(c.getSituacao() == 0){
                model.addRow(new Object[]{
                    c.getCod(),
                    c.getMedico(),
                    c.getPaciente(),
                    c.getData(),
                    c.getHora_inicio(),
                    c.getHora_final(),
                });
            }
        }
    }

