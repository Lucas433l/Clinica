    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        String crm = jT_CRM.getText();
        String[] aux = jF_Data.getText().split("/");
        String data = aux[2] +"-"+aux[1]+"-"+aux[0];
        ConsultaDAO dao = new ConsultaDAO();
        if(!crm.equals("")){
            readTable(dao.readForCRM(crm));
            
        }
        
        else if(!data.replace("-","").replace(" ","").equals("")){
            readTable(dao.readForDate(data));
        }
        else{
            readTable(dao.readAll());
        }
    }//GEN-LAST:event_jLabel5MouseClicked

    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        int linha = jTable.getSelectedRow();
        
        if(linha != -1)
        {
            int cod = (int) jTable.getValueAt(linha,0);
            int op = JOptionPane.showConfirmDialog(this,"Deseja realmente realizar essa consulta?");
            
            if(op == 0){
                new ConsultaDAO().situacao(1, cod);
                readTable(new ConsultaDAO().readAll());
            }
            else{
                JOptionPane.showMessageDialog(this,"Consulta não realizada.");
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha da tabela.");
        }
    }//GEN-LAST:event_jLabel2MouseClicked

    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        int linha = jTable.getSelectedRow();
        
        if(linha != -1)
        {
            int cod = (int) jTable.getValueAt(linha,0);
            int op = JOptionPane.showConfirmDialog(this,"Deseja realmente cancelar essa consulta?");
            
            if(op == 0){
                new ConsultaDAO().situacao(2, cod);
                readTable(new ConsultaDAO().readAll());
            }
            else{
                JOptionPane.showMessageDialog(this,"Consulta não será cancelada.");
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha da tabela.");
        }
    }//GEN-LAST:event_jLabel3MouseClicked

