    public void readTable(List<Ficha_Medica> dao){
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        model.setNumRows(0);
        
        for(Ficha_Medica f: dao){
            model.addRow(new Object[]{
                f.getCod(),
                f.getNome_medico(),
                f.getNome_paciente(),
                f.getData_consulta(),
                f.getQueixa(),
                f.getHipotese_diag()
            });
        }
    }
