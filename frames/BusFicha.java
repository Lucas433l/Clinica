    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        String nome = jT_Nome.getText();
        
        if(!nome.equals("")){
            readTable(new Ficha_MedicaDAO().readForName(nome));
        }
        else{
            readTable(new Ficha_MedicaDAO().readAll());
        }
    }//GEN-LAST:event_jLabel3MouseClicked

    private void jLabel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseClicked
        int linha = jTable.getSelectedRow();
        if(linha != -1){
            int cod = (int) jTable.getValueAt(linha,0);
            List<Ficha_Medica> f = new Ficha_MedicaDAO().readForCod(cod);
            Ficha_Medica ficha = f.get(0);
            new AlterarFicha(ficha).setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha da tabela.");
        }
    }//GEN-LAST:event_jLabel4MouseClicked

