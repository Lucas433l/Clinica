    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        String nome = jT_Nome.getText();
        String crm = jT_CRM.getText();
        
        if(!nome.equals("")){
            readTable(new MedicoDAO().readForName(nome));
        }
        
        else if(!crm.equals("")){
            crm += "/"+jC_UF.getSelectedItem();
            readTable(new MedicoDAO().readForCRM(crm));
        }
        
        else{
            readTable(new MedicoDAO().readAll());
        }
    }//GEN-LAST:event_jLabel8MouseClicked

    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        int linha = jTable.getSelectedRow();
        if(linha != -1){
            String crm = (String) jTable.getValueAt(linha,1);
            List<Medico> medico = new MedicoDAO().readForCRM(crm);
            Medico med = medico.get(0);
            new AlterarMed(med).setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha da tabela.");
        }
    }//GEN-LAST:event_jLabel9MouseClicked


