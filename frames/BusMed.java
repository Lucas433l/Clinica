    public void readTable(List<Medico> dao){
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        model.setNumRows(0);
        
        for(Medico m: dao){
            model.addRow(new Object[]{
                m.getNome(),
                m.getCRM(),
                m.getEndereco(),
                m.getSexo()
            });
        }
    }

