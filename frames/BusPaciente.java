    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        String nome = jT_Nome.getText();
        String cpf = jF_CPF.getText();
        PacienteDAO dao = new PacienteDAO();
        
        if(!nome.equals("")){
            readTable(dao.readForName(nome));
        }
        else if(!cpf.replace(".","").replace("-","").equals("")){
            readTable(dao.readForCPF(cpf));
        }
        else{
            readTable(dao.readAll());
        }
    }//GEN-LAST:event_jLabel8MouseClicked

    private void jLabel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel9MouseClicked
        int linha = jTable.getSelectedRow();
        
        if(linha != -1){
            String cpf = (String) jTable.getValueAt(linha,1);
            List<Paciente> p = new PacienteDAO().readForCPF(cpf);
            Paciente paciente = p.get(0);
            new AlterarPaciente(paciente).setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(this,"");
        }
        
    }//GEN-LAST:event_jLabel9MouseClicked


