    public void readTable(List<Paciente> dao){
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        model.setNumRows(0);
        
        for(Paciente p: dao){
            model.addRow(new Object[]{
                p.getNome(),
                p.getCPF(),
                p.getCod_con(),
                p.getCod_plano()
            });
        }
    }
