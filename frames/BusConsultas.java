    private void jLabel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MouseClicked
        String crm = jT_CRM.getText();
        String cpf = jF_CPF.getText();
        String[] aux = jF_Data.getText().split("/");
        String data = aux[2] +"-"+aux[1]+"-"+aux[0];
        ConsultaDAO dao = new ConsultaDAO();
        if(!crm.equals("")){
            crm += "/" + jC_UF.getSelectedItem();
            readTable(dao.readForCRM(crm));
            
        }
        else if(!cpf.replace(".","").replace("-","").replace(" ","").equals("")){
            readTable(dao.readForCPF(cpf));
        } 
        else if(!data.replace("-","").replace(" ","").equals("")){
            readTable(dao.readForDate(data));
        }
        else{
            readTable(dao.readAll());
        }
    }//GEN-LAST:event_jLabel10MouseClicked

