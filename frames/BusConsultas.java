    public void readTable(List<Consulta> dao){
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        model.setNumRows(0);
        
        for(Consulta c: dao){
            model.addRow(new Object[]{
                c.getCod(),
                c.getMedico(),
                c.getPaciente(),
                c.getData(),
                c.getHora_inicio(),
                c.getHora_final(),
                c.getSituacao()
            });
        }
    }
    
