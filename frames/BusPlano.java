    private void jL_apMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jL_apMouseClicked
        int linha = jTable.getSelectedRow();
        
        if(linha != -1){
            int cod = (int) jTable.getValueAt(linha ,1);
            if(cod != 1){
                PlanoDAO dao = new PlanoDAO();
                dao.delete(cod);
            }
            else{
                JOptionPane.showMessageDialog(this,"Não pode apagar o plano 1.");
            }
        }
        
        else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha da tabela.");
        }
    }//GEN-LAST:event_jL_apMouseClicked

    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        String tipo = jT_Tipo.getText();
        int cod_conv = (int) jS_Conv.getValue();
        int cod_plano = (int) jS_Plano.getValue();
        
        if(!tipo.equals("")){
            readTable(new PlanoDAO().readForTipo(tipo));
        }
        else if(cod_conv > 1){
            readTable(new PlanoDAO().readForCod_con(cod_conv));
        }
        else if(cod_plano > 1){
            readTable(new PlanoDAO().readForCod(cod_plano));
        }
        else{
            readTable(new PlanoDAO().readAll());
        }
    }//GEN-LAST:event_jLabel6MouseClicked

    private void jL_altMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jL_altMouseClicked
        int linha = jTable.getSelectedRow(); 
        if (linha != -1){
            int cod = (int) jTable.getValueAt(linha,1);
            if(cod == 1){
                JOptionPane.showMessageDialog(this,"Plano 1 não pode ser alterado.");
            }
            else{
                List<Plano> p = new PlanoDAO().readForCod(cod);
                Plano plano = p.get(0);
                new AlterarPlano(plano).setVisible(true);
            }
        }
        else{
            JOptionPane.showMessageDialog(this,"Selecione uma linha da tabela.");
        }
        
    }//GEN-LAST:event_jL_altMouseClicked

