    public void readTable(List<Plano> dao){
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        model.setNumRows(0);
        
        for(Plano p: dao){
            model.addRow(new Object[]{
                p.getCod_con(),
                p.getCod(),
                p.getTipo(),
                p.getDesc()
            });
        }
    }

